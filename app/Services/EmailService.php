    /**
     * Get classification service status
     */
    private function getClassificationStatus(): array
    {
        try {
            $stats = $this->classifier->getStats();
            return [
                'available' => true,
                'total_classified' => $stats['total_classified'] ?? 0,
            ];
        } catch (Exception $e) {
            return [
                'available' => false,
                'error' => $e->getMessage(),
            ];
        }
    }

    /**
     * Record an error for diagnostics
     */
    private function recordError(string $operation, string $message): void
    {
        $this->errors[] = [
            'operation' => $operation,
            'message' => $message,
            'timestamp' => now()->toIso8601String(),
        ];

        // Keep only last 10 errors
        if (count($this->errors) > 10) {
            array_shift($this->errors);
        }
    }

    /**
     * Get recorded errors
     */
    public function getErrors(): array
    {
        return $this->errors;
    }

    /**
     * Reset circuit breaker on Thunderbird service
     */
    public function resetThunderbirdCircuit(): void
    {
        $this->thunderbird->resetCircuit();
    }
