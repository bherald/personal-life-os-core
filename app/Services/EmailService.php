<?php

namespace App\Services;

use App\Services\ThunderbirdService;
use App\Services\EmailClassificationService;
use Illuminate\Support\Facades\DB;
use Illuminate\Support\Facades\Log;
use Illuminate\Support\Facades\Cache;
use Exception;

/**
 * Unified Email Service (EA2)
 *
 * Provides comprehensive email functionality for the PLOS framework:
 * - Full email client access (read, search, send)
 * - AI-powered classification and categorization
 * - Draft queue with human approval workflow
 * - Configurable folder monitoring
 *
 * Architecture:
 * EmailService
 *   ├── ThunderbirdService (MCP communication)
 *   └── EmailClassificationService (AI features)
 *
 * Draft Sources:
 * - ai_reply: AI-generated reply suggestions
 * - workflow: Emails generated by workflow nodes
 * - data_removal: Data broker removal emails
 * - manual: (future) Manual composition via UI
 */
class EmailService
{
    private ThunderbirdService $thunderbird;
    private EmailClassificationService $classifier;
    private ?EmailRateLimitService $rateLimitService = null;

    // Error tracking
    private array $errors = [];
    private int $operationCount = 0;

    // Settings cache key
    private const SETTINGS_CACHE_KEY = 'email_service_settings';
    /** @see config/timeouts.php cache.email */

    public function __construct(
        ThunderbirdService $thunderbird,
        EmailClassificationService $classifier,
        ?EmailRateLimitService $rateLimitService = null
    ) {
        $this->thunderbird = $thunderbird;
        $this->classifier = $classifier;
        $this->rateLimitService = $rateLimitService;
    }

    // =========================================================================
    // CONNECTION & STATUS
    // =========================================================================

    /**
     * Check if email service is available
     */
    public function isAvailable(): bool
    {
        return $this->thunderbird->isAvailable();
    }

    /**
     * Get comprehensive service status
     */
    public function getStatus(): array
    {
        $thunderbirdStatus = $this->thunderbird->getConnectionStatus();

        $status = [
            'available' => $thunderbirdStatus['available'],
            'thunderbird' => $thunderbirdStatus,
            'classification' => $this->getClassificationStatus(),
            'draft_queue' => $this->getDraftQueueStats(),
            'settings' => $this->getSettings(),
            'errors' => $this->errors,
            'operation_count' => $this->operationCount,
        ];

        return $status;
    }

    /**
     * Get email statistics
     */
    public function getStats(): array
    {
        $stats = [
            'thunderbird' => null,
            'classification' => null,
            'drafts' => null,
        ];

        try {
            if ($this->thunderbird->isAvailable()) {
                $stats['thunderbird'] = $this->thunderbird->getStats();
            }
        } catch (Exception $e) {
            $this->recordError('stats_thunderbird', $e->getMessage());
        }

        try {
            $stats['classification'] = $this->classifier->getStats();
        } catch (Exception $e) {
            $this->recordError('stats_classification', $e->getMessage());
        }

        $stats['drafts'] = $this->getDraftQueueStats();

        return $stats;
    }

