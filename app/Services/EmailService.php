    /**
     * Classify an email using AI
     *
     * @param array $email Email data
     * @return array Classification result
     */
    public function classifyEmail(array $email): array
    {
        $this->operationCount++;

        try {
            return $this->classifier->classifyEmail($email);
        } catch (Exception $e) {
            $this->recordError('classify', $e->getMessage());
            throw $e;
        }
    }

    /**
     * Classify recent emails from a folder
     *
     * @param string $folder Folder name
     * @param int $limit Number to classify
     * @return array Classification results
     */
    public function classifyRecentEmails(string $folder = 'Inbox', int $limit = 10): array
    {
        $this->operationCount++;

        try {
            $emails = $this->getRecentEmails($folder, $limit);
            $results = [];

            foreach ($emails as $email) {
                try {
                    $result = $this->classifier->classifyEmail($email);
                    $results[] = [
                        'email' => $email,
                        'classification' => $result['classification'],
                        'success' => $result['success'],
                    ];
                } catch (Exception $e) {
                    $results[] = [
                        'email' => $email,
                        'error' => $e->getMessage(),
                        'success' => false,
                    ];
                }
            }

            return [
                'total' => count($emails),
                'classified' => count(array_filter($results, fn($r) => $r['success'])),
                'results' => $results,
            ];

        } catch (Exception $e) {
            $this->recordError('classify_recent', $e->getMessage());
            throw $e;
        }
    }

