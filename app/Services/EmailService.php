    /**
     * Queue an email draft for human approval
     *
     * @param array $email Email data (to, subject, body, etc.)
     * @param string $source Source identifier (ai_reply, workflow, data_removal)
     * @param array $options Additional options (priority, related_type, related_id)
     * @return int Draft ID
     */
    public function queueDraft(array $email, string $source = 'workflow', array $options = []): int
    {
        $this->operationCount++;

        // Validate required fields
        $required = ['to', 'subject', 'body'];
        foreach ($required as $field) {
            if (empty($email[$field])) {
                throw new Exception("Missing required field for draft: {$field}");
            }
        }

        // Validate email format
        if (!filter_var($email['to'], FILTER_VALIDATE_EMAIL)) {
            throw new Exception("Invalid email address: {$email['to']}");
        }

        $sql = "INSERT INTO email_reply_drafts
                (original_message_id, source, `to`, from_address, cc, bcc, subject, body,
                 ai_suggestions, status, priority, template_id, workflow_execution_id,
                 related_type, related_id, metadata, created_at, updated_at)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

        try {
            DB::insert($sql, [
                $email['original_message_id'] ?? null,
                $source,
                $email['to'],
                $email['from'] ?? null,
                $email['cc'] ?? null,
                $email['bcc'] ?? null,
                $email['subject'],
                $email['body'],
                $email['ai_suggestions'] ?? null,
                'draft',
                $options['priority'] ?? 'normal',
                $options['template_id'] ?? null,
                $options['workflow_execution_id'] ?? null,
                $options['related_type'] ?? null,
                $options['related_id'] ?? null,
                json_encode($email['metadata'] ?? []),
                now(),
                now(),
            ]);

            $draftId = (int) DB::getPdo()->lastInsertId();

            // Record draft version
            try {
                app(\App\Services\EmailEnhancementsService::class)
                    ->saveDraftVersion($draftId, $email['body'], 'ai', 'created');
            } catch (\Exception $e) {
                Log::warning('EmailService: Failed to save draft version', ['error' => $e->getMessage()]);
            }

            Log::info('EmailService: Draft queued', [
                'draft_id' => $draftId,
                'source' => $source,
                'to' => $email['to'],
                'subject' => $email['subject'],
            ]);

            return $draftId;

        } catch (Exception $e) {
            $this->recordError('queue_draft', $e->getMessage());
            throw $e;
        }
    }

    /**
     * Get pending drafts from queue
     *
     * @param array $filters Optional filters (source, priority, limit)
     * @return array Pending drafts
     */
    public function getPendingDrafts(array $filters = []): array
    {
        $sql = "SELECT * FROM email_reply_drafts WHERE status = 'pending'";
        $params = [];

        if (!empty($filters['source'])) {
            $sql .= " AND source = ?";
            $params[] = $filters['source'];
        }

        if (!empty($filters['priority'])) {
            $sql .= " AND priority = ?";
            $params[] = $filters['priority'];
        }

        $sql .= " ORDER BY
            CASE priority
                WHEN 'urgent' THEN 1
                WHEN 'high' THEN 2
                WHEN 'normal' THEN 3
                WHEN 'low' THEN 4
            END,
            created_at ASC";

        $limit = $filters['limit'] ?? 50;
        $sql .= " LIMIT " . (int) $limit;

        return DB::select($sql, $params);
    }

    /**
     * Get a specific draft by ID
     *
     * @param int $draftId Draft ID
     * @return object|null Draft record or null
     */
    public function getDraft(int $draftId): ?object
    {
        $result = DB::selectOne(
            "SELECT * FROM email_reply_drafts WHERE id = ?",
            [$draftId]
        );

        return $result ?: null;
    }

    /**
     * Update a draft
     *
     * @param int $draftId Draft ID
     * @param array $updates Fields to update
     * @return bool Success
     */
    public function updateDraft(int $draftId, array $updates): bool
    {
        $allowedFields = ['to', 'from_address', 'cc', 'bcc', 'subject', 'body', 'priority'];
        $setClauses = [];
        $params = [];

        foreach ($updates as $field => $value) {
            if (in_array($field, $allowedFields)) {
                $setClauses[] = "`{$field}` = ?";
                $params[] = $value;
            }
        }

        if (empty($setClauses)) {
            return false;
        }

        $setClauses[] = "updated_at = ?";
        $params[] = now();
        $params[] = $draftId;

        $sql = "UPDATE email_reply_drafts SET " . implode(', ', $setClauses) . " WHERE id = ?";

        $updated = DB::update($sql, $params) > 0;

        // Record draft version on edit
        if ($updated && isset($updates['body'])) {
            try {
                app(\App\Services\EmailEnhancementsService::class)
                    ->saveDraftVersion($draftId, $updates['body'], 'human', 'edited');
            } catch (\Exception $e) {
                Log::warning('EmailService: Failed to save draft version', ['error' => $e->getMessage()]);
            }
        }

        return $updated;
    }

    /**
     * Approve and send a draft
     *
     * @param int $draftId Draft ID
     * @return array Result with success status
     */
    public function approveDraft(int $draftId): array
    {
        $this->operationCount++;

        $draft = $this->getDraft($draftId);

        if (!$draft) {
            return [
                'success' => false,
                'error' => "Draft not found: {$draftId}",
            ];
        }

        if ($draft->status !== 'pending') {
            return [
                'success' => false,
                'error' => "Draft already processed (status: {$draft->status})",
            ];
        }

        // Attempt to send (with full suppression + rate limit checks)
        $sendResult = $this->sendEmailWithAllChecks(
            $draft->to,
            $draft->subject,
            $draft->body,
            $draft->from_address
        );

        if ($sendResult['success']) {
            // Update draft status
            DB::update(
                "UPDATE email_reply_drafts
                 SET status = 'sent', approved_at = ?, sent_at = ?, updated_at = ?
                 WHERE id = ?",
                [now(), now(), now(), $draftId]
            );

            // Record approved version
            try {
                app(\App\Services\EmailEnhancementsService::class)
                    ->saveDraftVersion($draftId, $draft->body, 'human', 'approved');
            } catch (\Exception $e) {
                Log::warning('EmailService: Failed to save draft version', ['error' => $e->getMessage()]);
            }

            Log::info('EmailService: Draft approved and sent', [
                'draft_id' => $draftId,
                'to' => $draft->to,
            ]);

            return [
                'success' => true,
                'draft_id' => $draftId,
                'send_result' => $sendResult['result'],
            ];

        } else {
            // Mark as failed
            DB::update(
                "UPDATE email_reply_drafts
                 SET status = 'failed', metadata = JSON_SET(COALESCE(metadata, '{}'), '$.send_error', ?), updated_at = ?
                 WHERE id = ?",
                [$sendResult['error'], now(), $draftId]
            );

            return [
                'success' => false,
                'error' => $sendResult['error'],
                'draft_id' => $draftId,
            ];
        }
    }

    /**
     * Reject a draft
     *
     * @param int $draftId Draft ID
     * @param string|null $reason Optional rejection reason
     * @return bool Success
     */
    public function rejectDraft(int $draftId, ?string $reason = null): bool
    {
        $this->operationCount++;

        $metadata = $reason ? json_encode(['rejection_reason' => $reason]) : null;

        $updated = DB::update(
            "UPDATE email_reply_drafts
             SET status = 'rejected', metadata = COALESCE(?, metadata), updated_at = ?
             WHERE id = ? AND status = 'pending'",
            [$metadata, now(), $draftId]
        );

        if ($updated) {
            // Record rejected version
            try {
                app(\App\Services\EmailEnhancementsService::class)
                    ->saveDraftVersion($draftId, '', 'human', 'rejected');
            } catch (\Exception $e) {
                Log::warning('EmailService: Failed to save draft version', ['error' => $e->getMessage()]);
            }

            Log::info('EmailService: Draft rejected', [
                'draft_id' => $draftId,
                'reason' => $reason,
            ]);
        }

        return $updated > 0;
    }

    /**
     * Get draft queue statistics
     */
    public function getDraftQueueStats(): array
    {
        $stats = DB::select("
            SELECT
                status,
                source,
                COUNT(*) as count
            FROM email_reply_drafts
            GROUP BY status, source
        ");

        $byStatus = [];
        $bySource = [];

        foreach ($stats as $row) {
            $byStatus[$row->status] = ($byStatus[$row->status] ?? 0) + $row->count;
            $bySource[$row->source] = ($bySource[$row->source] ?? 0) + $row->count;
        }

        $pendingCount = $byStatus['pending'] ?? 0;
        $urgentCount = DB::selectOne(
            "SELECT COUNT(*) as count FROM email_reply_drafts WHERE status = 'pending' AND priority = 'urgent'"
        )->count ?? 0;

        return [
            'pending' => $pendingCount,
            'urgent' => $urgentCount,
            'by_status' => $byStatus,
            'by_source' => $bySource,
            'total' => array_sum($byStatus),
        ];
    }

