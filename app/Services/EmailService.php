    public function getRecentEmails(string $folder = 'Inbox', int $limit = 10): array
    {
        $this->operationCount++;

        try {
            return $this->thunderbird->getRecentMessages($folder, $limit);
        } catch (Exception $e) {
            $this->recordError('get_recent', $e->getMessage());
            throw $e;
        }
    }

    // =========================================================================
    // EMAIL SENDING
    // =========================================================================

    /**
     * Send email immediately via Thunderbird
     *
     * @param string $to Recipient email
     * @param string $subject Email subject
     * @param string $body Email body
     * @param string|null $from Sender mailbox (optional, uses default)
     * @return array Send result
     */
    public function sendEmail(string $to, string $subject, string $body, ?string $from = null): array
    {
        $this->operationCount++;

        $params = [
            'to' => $to,
            'subject' => $subject,
            'body' => $body,
        ];

        if ($from) {
            $params['mailbox'] = $from;
        }

        try {
            $result = $this->thunderbird->sendEmail($params);

            // Log successful send
            Log::info('EmailService: Email sent', [
                'to' => $to,
                'subject' => $subject,
                'result' => $result,
            ]);

            return [
                'success' => true,
                'result' => $result,
            ];

        } catch (Exception $e) {
            $this->recordError('send_email', $e->getMessage());

            Log::error('EmailService: Send failed', [
                'to' => $to,
                'subject' => $subject,
                'error' => $e->getMessage(),
            ]);

            return [
                'success' => false,
                'error' => $e->getMessage(),
            ];
        }
    }

    /**
     * Send email with bounce check
     *
     * Checks suppression list before sending. If email is suppressed,
     * returns error without attempting to send.
     *
    /**
     * Send email with full pre-send checks (bounce + rate limiting)
     *
     * This is the recommended method for sending emails as it performs:
     * - Suppression list check (bounces/complaints)
     * - Rate limit check (daily/hourly limits)
     * - Domain throttling (delays for problematic domains)
     * - Cooldown verification
     *
     * @param string $to Recipient email
     * @param string $subject Email subject
     * @param string $body Email body
     * @param string|null $from Sender mailbox (optional, uses default)
     * @return array Result with success status and details
     */
    public function sendEmailWithAllChecks(string $to, string $subject, string $body, ?string $from = null): array
    {
        $this->operationCount++;

        // Determine sender mailbox
        $mailbox = $from ?? $this->getDefaultMailbox();
        if (!$mailbox) {
            return [
                'success' => false,
                'error' => 'No sender mailbox specified and no default configured',
            ];
        }

        // Extract recipient domain
        $recipientDomain = $this->extractDomain($to);

        // Check rate limits
        if ($this->rateLimitService && !$this->rateLimitService->canSend($mailbox, $recipientDomain)) {
            $quota = $this->rateLimitService->getRemainingQuota($mailbox);

            Log::warning('EmailService: Rate limit blocked send', [
                'mailbox' => $mailbox,
                'to' => $to,
                'quota' => $quota,
            ]);

            return [
                'success' => false,
                'error' => $quota['in_cooldown']
                    ? 'Mailbox is in cooldown period'
                    : 'Rate limit exceeded (daily: ' . $quota['daily_remaining'] . ' remaining, hourly: ' . $quota['hourly_remaining'] . ' remaining)',
                'suppressed' => false,
                'rate_limited' => true,
                'quota' => $quota,
            ];
        }

        // Apply domain-specific delay if needed
        if ($this->rateLimitService) {
            $delay = $this->rateLimitService->getDomainDelay($recipientDomain);
            if ($delay > 0) {
                usleep($delay * 1000); // Convert ms to microseconds
            }
        }

        // Send the email
        $result = $this->sendEmail($to, $subject, $body, $from);

        // Record the send if successful
        if ($result['success'] && $this->rateLimitService) {
            $this->rateLimitService->recordSend($mailbox, $recipientDomain);
        }

        return array_merge($result, [
            'suppressed' => false,
            'rate_limited' => false,
        ]);
    }

    /**
     * Get rate limit service for direct access
     */
    public function getRateLimitService(): ?EmailRateLimitService
    {
        return $this->rateLimitService;
    }

    /**
     * Get remaining email quota for a mailbox
     *
     * @param string|null $mailbox Mailbox to check (null = default)
     * @return array|null Quota info or null if rate limiting not configured
     */
    public function getEmailQuota(?string $mailbox = null): ?array
    {
        if (!$this->rateLimitService) {
            return null;
        }

        $mailbox = $mailbox ?? $this->getDefaultMailbox();
        if (!$mailbox) {
            return null;
        }

        return $this->rateLimitService->getRemainingQuota($mailbox);
    }

    /**
     * Extract domain from email address
     */
    private function extractDomain(string $email): string
    {
        $parts = explode('@', strtolower(trim($email)));
        return $parts[1] ?? 'unknown';
    }

    /**
     * Get default mailbox from settings
     */
    private function getDefaultMailbox(): ?string
    {
        $settings = $this->getSettings();
        return $settings['default_mailbox'] ?? config('mail.from.address');
    }

