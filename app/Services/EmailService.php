    /**
     * Get email service settings
     */
    public function getSettings(): array
    {
        return Cache::remember(self::SETTINGS_CACHE_KEY, config('timeouts.cache.email', 3600), function () {
            $settings = DB::select("SELECT setting_key, setting_value FROM email_settings");

            $result = [
                'monitored_folders' => ['Inbox'],
                'default_mailbox' => null,
                'auto_classify' => true,
                'classification_batch_size' => 10,
            ];

            foreach ($settings as $row) {
                $value = $row->setting_value;

                // Try to decode JSON values
                if ($value !== null) {
                    $decoded = json_decode($value, true);
                    $result[$row->setting_key] = (json_last_error() === JSON_ERROR_NONE) ? $decoded : $value;
                } else {
                    $result[$row->setting_key] = null;
                }
            }

            return $result;
        });
    }

    /**
     * Get monitored folders
     */
    public function getMonitoredFolders(): array
    {
        $settings = $this->getSettings();
        return $settings['monitored_folders'] ?? ['Inbox'];
    }

    /**
     * Update monitored folders
     *
     * @param array $folders List of folder names to monitor
     */
    public function setMonitoredFolders(array $folders): void
    {
        $this->saveSetting('monitored_folders', $folders);
    }

    /**
     * Update a setting
     *
     * @param string $key Setting key
     * @param mixed $value Setting value
     */
    public function saveSetting(string $key, $value): void
    {
        $jsonValue = is_array($value) ? json_encode($value) : (string) $value;

        // Upsert setting
        $exists = DB::selectOne(
            "SELECT id FROM email_settings WHERE setting_key = ?",
            [$key]
        );

        if ($exists) {
            DB::update(
                "UPDATE email_settings SET setting_value = ?, updated_at = ? WHERE setting_key = ?",
                [$jsonValue, now(), $key]
            );
        } else {
            DB::insert(
                "INSERT INTO email_settings (setting_key, setting_value, created_at, updated_at) VALUES (?, ?, ?, ?)",
                [$key, $jsonValue, now(), now()]
            );
        }

        // Clear cache
        Cache::forget(self::SETTINGS_CACHE_KEY);

        Log::info('EmailService: Setting updated', [
            'key' => $key,
            'value' => $value,
        ]);
    }

